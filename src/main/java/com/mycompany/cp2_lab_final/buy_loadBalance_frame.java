    private void topup_bttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_topup_bttnActionPerformed
       
        
        int topup;
        try{
             topup = Integer.parseInt(topup_txt.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Please enter an integer number");
            return;
        }
            if(topup>100000){
            JOptionPane.showMessageDialog(rootPane, "You can only add up to 100,000 as balance.");
            return;
            }
            else if(topup<=0){
            JOptionPane.showMessageDialog(rootPane, "The added balance must be greater than 0.");
            return;
            }
            walletManager.TopUpBalance(topup);
           wframe.setVisible(false);
           wframe = new wallet_frame();
           wframe.setVisible(true);
           
           
    }//GEN-LAST:event_topup_bttnActionPerformed

    private void buy_bttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buy_bttnActionPerformed
         int adet;
        try{
             adet = Integer.parseInt(buy_txt.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Please enter an integer number");
            return;
        }
        
        if(adet <=0){
        JOptionPane.showMessageDialog(rootPane,"Please enter a positive numeric value");
        return;
        }
        try {
            
            walletManager.buy(jComboBox1.getSelectedIndex()+1, adet);
        } catch (IOException ex) {
            Logger.getLogger(buy_loadBalance_frame.class.getName()).log(Level.SEVERE, null, ex);
        }
           wframe.setVisible(false);
           wframe = new wallet_frame();
           wframe.setVisible(true);
        
    }//GEN-LAST:event_buy_bttnActionPerformed

