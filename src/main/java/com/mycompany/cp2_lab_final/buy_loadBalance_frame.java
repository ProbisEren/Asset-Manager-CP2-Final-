    public buy_loadBalance_frame(String selectedName) {
        wframe.setVisible(true);
    initComponents();
    int i = 1;
    boolean matched = false;

    while (valuableManager.name_of(i) != null) {
        String name = valuableManager.name_of(i);
        jComboBox1.addItem(name);
        if (name.equals(selectedName)) {
            matched = true;
        }
        i++;
    }

    // Eğer listedeki veriler arasında varsa seç
    if (matched) {
        jComboBox1.setSelectedItem(selectedName);
    }
}


